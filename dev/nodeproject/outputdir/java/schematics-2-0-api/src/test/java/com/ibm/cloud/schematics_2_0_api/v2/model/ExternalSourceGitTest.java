package com.ibm.cloud.schematics_2_0_api.v2.model;

import com.ibm.cloud.schematics_2_0_api.v2.model.ExternalSourceGit;
import com.ibm.cloud.schematics_2_0_api.v2.utils.TestUtilities;
import com.ibm.cloud.sdk.core.service.model.FileWithMetadata;
import java.io.InputStream;
import java.util.HashMap;
import java.util.List;
import org.testng.annotations.Test;
import static org.testng.Assert.*;

/**
 * Unit test class for the ExternalSourceGit model.
 */
public class ExternalSourceGitTest {
  final HashMap<String, InputStream> mockStreamMap = TestUtilities.createMockStreamMap();
  final List<FileWithMetadata> mockListFileWithMetadata = TestUtilities.creatMockListFileWithMetadata();

  @Test
  public void testExternalSourceGit() throws Throwable {
    ExternalSourceGit externalSourceGitModel = new ExternalSourceGit.Builder()
      .computedGitRepoUrl("testString")
      .gitRepoUrl("testString")
      .gitToken("testString")
      .gitRepoFolder("testString")
      .gitRelease("testString")
      .gitBranch("testString")
      .add("foo", "testString")
      .build();
    assertEquals(externalSourceGitModel.getComputedGitRepoUrl(), "testString");
    assertEquals(externalSourceGitModel.getGitRepoUrl(), "testString");
    assertEquals(externalSourceGitModel.getGitToken(), "testString");
    assertEquals(externalSourceGitModel.getGitRepoFolder(), "testString");
    assertEquals(externalSourceGitModel.getGitRelease(), "testString");
    assertEquals(externalSourceGitModel.getGitBranch(), "testString");
    assertEquals(externalSourceGitModel.get("foo"), "testString");

    String json = TestUtilities.serialize(externalSourceGitModel);

    ExternalSourceGit externalSourceGitModelNew = TestUtilities.deserialize(json, ExternalSourceGit.class);
    assertTrue(externalSourceGitModelNew instanceof ExternalSourceGit);
    assertEquals(externalSourceGitModelNew.getComputedGitRepoUrl(), "testString");
    assertEquals(externalSourceGitModelNew.getGitRepoUrl(), "testString");
    assertEquals(externalSourceGitModelNew.getGitToken(), "testString");
    assertEquals(externalSourceGitModelNew.getGitRepoFolder(), "testString");
    assertEquals(externalSourceGitModelNew.getGitRelease(), "testString");
    assertEquals(externalSourceGitModelNew.getGitBranch(), "testString");
    assertEquals(externalSourceGitModelNew.get("foo"), "testString");
  }
}